    public function search(Request $request)
    {
        $request->validate([
            'search'=>'required|min:2'
        ]);
        $q = $request->search;
        $dishes = Dish::where('name', 'LIKE', "%$q%")->orWhere('description','LIKE', "%$q%")->paginate(12);
        return view('employee.search', compact('dishes'));
